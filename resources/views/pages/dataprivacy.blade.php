                    <h2 class="text-base font-bold text-indigo-900 mb-2">VI. Rights of the Data Subject</h2>
                    <p class="mb-2">Under the Data Privacy Act, data subjects have the right to:</p>
                    <ul class="list-disc list-inside space-y-1 text-gray-600">
                         <li>Be informed about the collection and processing of their personal data</li>
                         <li>Access their personal data held by LGTAC</li>
                         <li>Object to the processing of their data under certain circumstances</li>
                         <li>Request correction or deletion of inaccurate or outdated information</li>
                         <li>Withdraw consent at any time, subject to legal and contractual obligations</li>
                         <li>Lodge a complaint with the National Privacy Commission (NPC)</li>
                    </ul>
