                    <h2 class="text-base font-bold text-indigo-900 mb-2">I. Purpose</h2>
                    <p>
                         This Data Privacy Waiver is executed to comply with the Data Privacy Act of 2012 (Republic Act No. 10173) and its Implementing Rules and Regulations. It aims to inform and secure the consent of trainees, assessors, employees, and other stakeholders of Lumen Generation Training and Assessment Center <span class="font-semibold">(LGTAC)</span> regarding the collection, use, storage, and processing of their personal data.
                    </p>
               </section>

               <hr class="border-gray-200" />

               <!-- Section II -->
               <section>
                    <h2 class="text-base font-bold text-indigo-900 mb-2">II. Personal Data Collected</h2>
                    <p class="mb-2">LGTAC may collect and process the following personal information:</p>
                    <ul class="list-disc list-inside space-y-1 text-gray-600">
                         <li>Full name, date of birth, and contact details (address, phone number, email)</li>
                         <li>Government-issued identification numbers (e.g., TIN, SSS, PhilHealth, Passport, Driver's License)</li>
                         <li>Educational background and employment history</li>
                         <li>Training and assessment records, certificates, and evaluation results</li>
                         <li>Photographs, video recordings, and biometric data (if applicable)</li>
                         <li>Emergency contact information</li>
                    </ul>
               </section>

               <hr class="border-gray-200" />

               <!-- Section III -->
               <section>
                    <h2 class="text-base font-bold text-indigo-900 mb-2">III. Purpose of Data Collection</h2>
                    <p class="mb-2">The personal data collected shall be used for the following purposes:</p>
                    <ul class="list-disc list-inside space-y-1 text-gray-600">
                         <li>Enrollment, training, and assessment administration</li>
                         <li>Certification and record-keeping purposes</li>
                         <li>Compliance with TESDA and other regulatory requirements</li>
                         <li>Communication regarding training schedules, updates, and announcements</li>
                         <li>Verification of credentials and issuance of certificates</li>
                         <li>Internal research, monitoring, and quality assurance</li>
                         <li>Security and safety management within the premises</li>
                         <li>Academic, institutional, and industry-related research aimed at improving training programs, assessment methods, and employment outcomes</li>
                         <li>Market research and analysis to identify trends, improve services, and develop programs that meet industry and trainee needs</li>
                    </ul>
               </section>

               <hr class="border-gray-200" />

               <!-- Section IV -->
               <section>
                    <h2 class="text-base font-bold text-indigo-900 mb-2">IV. Data Sharing and Disclosure</h2>
                    <p class="mb-2">Personal data may be shared with the following entities when necessary:</p>
                    <ul class="list-disc list-inside space-y-1 text-gray-600">
                         <li>Technical Education and Skills Development Authority (TESDA)</li>
                         <li>Government agencies requiring verification or compliance reports</li>
                         <li>Partner institutions or employers for employment or apprenticeship opportunities</li>
                         <li>Authorized third-party service providers for data processing and storage</li>
                         <li>Research collaborators or institutions conducting studies related to technical-vocational education and training, provided that data shared will be anonymized or aggregated to protect individual identities</li>
                         <li>Market research partners or consultants engaged by LGTAC to analyze data for service improvement, provided that all shared data will be anonymized and used solely for legitimate research and development purposes</li>
                    </ul>
                    <p class="mt-3 text-gray-500 italic">LGTAC ensures that all third parties adhere to strict confidentiality and data protection standards.</p>
               </section>

               <hr class="border-gray-200" />

               <!-- Section V -->
               <section>
                    <h2 class="text-base font-bold text-indigo-900 mb-2">V. Data Storage and Retention</h2>
                    <p>
                         All personal data shall be securely stored in physical and electronic formats. Access is limited to authorized personnel only. Data will be retained for as long as necessary to fulfill the purposes stated above or as required by law. After the retention period, data will be securely disposed of or anonymized.
                    </p>
               </section>

               <hr class="border-gray-200" />

